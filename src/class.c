#include "class.h"

enum e_class	classof(size_t sz)
{
	if (sz <= SIZE_TINY)
		return CLASS_TINY;
	if (sz <= SIZE_SMALL)
		return CLASS_SMALL;
	return CLASS_LARGE;
}
